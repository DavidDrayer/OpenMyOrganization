<a name="editText"/>
<h1>Editer une zone de texte</h1>
<div class="content">
  <p>D&egrave;s que vous avez <a href="#activateEditor">activ&eacute; l'&eacute;diteur</a>, vous pouvez modifier un certain nombre de zones de texte dans la page courante. Les modifications que vous pouvez faire sont nombreuses et incluent autant des op&eacute;rations sur le texte, les images et les liens. A l'issue des modifications, n'oubliez pas de sauver votre travail. </p>
  <p>Voici les &eacute;tapes &agrave; suivre pour modifier le contenu d'une zone de texte:</p>
  <ul>
    <li>
      En promenant le curseur de la souris sur la page, certaines zones s'illuminent en couleur (jaune la plupart du temps). Cliquez sur la zone illumin&eacute;e que vous d&eacute;sirez modifier. Si une zone ne s'illumine pas, c'est qu'elle n'est pas &eacute;ditable ou que le curseur de la souris n'est pas bien plac&eacute; dessus.<br>
        <div align="center"><img src="../images/help/cadreSelect.gif" alt="L zone à éditer est ici mise en évidence avec un cadre plus clair." width="160" height="120" border="1">    </div>
    </li>
    <li>
      Lorsqu'elle est cliqu&eacute;e, la zone de texte s'entoure d'un cadre jaune, audessous duquel il y a quelques boutons. C'est le cadre d'&eacute;dition.<br>
        <div align="center"><img src="../images/help/cadreJaune.gif" width="156" height="126" border="1" alt="Voici à quoi ressemble une zone sélectionnée. La taille de la zone peut varier">    </div>
    </li>
    <li>
      Le cadre d'&eacute;dition est une copie de la zone que vous &ecirc;tes en train d'&eacute;diter. Cela permetra de confirmer ou d'annuler les modifications effectu&eacute;es par la suite, mais &eacute;galement de comparer l'ancien contenu et le nouveau en d&eacute;pla&ccedil;ant le cadre d'&eacute;dition &agrave; l'aide de la souris, en glissant-d&eacute;pla&ccedil;ant la barre de menu du bas.<br>
        <div align="center"><img src="../images/help/cadreMove.gif" alt="Ici le cadre a été déplacé léàgèrement en bas à droite, à l'aide du curseur." width="198" height="160" border="1">    </div>
    </li>
    <li>Vous avez tout le loisir de <a href="#modifyText">modifier le contenu de la zone de texte</a>, en utilisant les outils &agrave; dispositions, en s'assurant de respecter la charte graphique du site et/ou les directives de votre entreprise. </li>
    <li>
      D&egrave;s que vous avez termin&eacute; vos modifications, choisissez de sauver ou d'annuler vos modifications &agrave; l'aide des boutons en bas du cadre. Lorsque vous sauvez, le nouveau contenu est envoy&eacute; au site Internet et est sauv&eacute; dans la base de donn&eacute;es. Si vous annuler, l'ancien contenu est restaur&eacute;. <br>
        <div align="center"><span style="border:1px solid black;" title="Choisissez l'un de ces deux boutons lorsque vous voulez quitter le cadre d'édition."><img src="../images/paletteleft.gif" width="5" height="18"><img src="../images/editeurvalider.gif" width="56" height="18"><img src="../images/palettefermer.gif" width="19" height="18"><img src="../images/paletteright.gif" width="5" height="18"></span></div>
    </li>
    <li>Aucune des modifications effectu&eacute;es (modifier le texte, ins&eacute;rer une image, cr&eacute;er un lien,...) n'est sauv&eacute;e tant que vous n'avez pas appuyer sur le bouton valider. Attention donc &agrave; ne pas quitter la page Internet sans avoir sauv&eacute;. </li>
  </ul>
</div>

<a name="modifyText"/>
<h1>Modifier le texte d'une zone</h1>
<div class="content">
  <p>D&egrave;s que vous avez <a href="#editText">s&eacute;lectionn&eacute; une zone de texte</a> &agrave; &eacute;diter , vous pouvez en modifier le contenu textuel. Pour ce faire:</p>
  <ul>
    <li>
      Positionner le curseur &agrave; l'endroit o&ugrave; vous d&eacute;sirez &eacute;crire du texte en cliquant avec la souris.<br>
        <div align="center"><img src="../images/help/positonCurseur.gif" alt="Le curseur d'insertion de texte a été placé à la fin du texte, avant le point." width="156" height="126" border="1">    </div>
    </li>
    <li>
      Ecrivez le texte que vous d&eacute;sirez ins&eacute;rer.<br>
        <div align="center"><img src="../images/help/nouveauTexte.gif" alt="Et un nouveau texte a été écrit." width="156" height="139" border="1">    </div>
    </li>
    <li>Pour supprimer du texte, vous pouvez utiliser les touches <span class="key">delete</span> et <span class="key">backspace</span> </li>
    <li>
      Pour supprimer un grand bloc de texte, s&eacute;lectionnez le bloc et appuyez sur la touche  <span class="key">delete</span>.<br>
        <div align="center"><img src="../images/help/selectionTexte.gif" alt="Ici le texte a été sélectionné à l'aide de la souris." width="156" height="139" border="1">    </div>
    </li>
  </ul>
